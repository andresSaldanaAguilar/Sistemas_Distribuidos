    void setVisible(boolean b, int i) throws NotBoundException {
        try {
            setVisible(true);
            String stringInput = JOptionPane.showInputDialog("Id de la Calificacion a modificar");
            if (stringInput != null) {
                int id = Integer.valueOf(stringInput);
                CalificacionesInt calificacionesController = (CalificacionesInt) Naming.lookup("rmi://localhost/calificacionesController");
                Calificaciones c = calificacionesController.selectoneCalificaciones(id);
                if (c != null) {
                    this.puntaje.setText(String.valueOf(c.getPuntuaje()));
                    this.idAlumno.setText(String.valueOf(c.getAlumno().getMatricula()));
                    this.idExamen.setText(String.valueOf(c.getExamen().getIdExamen()));
                    this.jButton3.setText("Cambiar");
                    banderaCambio = 1;
                    idCambio = id;
                } else {
                    JOptionPane.showMessageDialog(null, "Id inv치lido");
                    this.setVisible(false);
                    this.puntaje.setText("");
                    this.idAlumno.setText("");
                    this.idExamen.setText("");
                    this.jButton4.setText("Aceptar");
                    banderaCambio = 0;
                }
            } else {
                this.setVisible(false);
                this.puntaje.setText("");
                this.idAlumno.setText("");
                this.idExamen.setText("");
                this.jButton4.setText("Aceptar");
                banderaCambio = 0;
            }

        } catch (MalformedURLException | RemoteException ex) {
            Logger.getLogger(Formulario_Centrodetrabajo_Alta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
