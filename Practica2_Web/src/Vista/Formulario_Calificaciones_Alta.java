    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            AlumnoInt alumnoController = (AlumnoInt) Naming.lookup("rmi://localhost/alumnoController");
            ExamenInt examenController = (ExamenInt) Naming.lookup("rmi://localhost/examenController");
            CalificacionesInt calificacionesController = (CalificacionesInt) Naming.lookup("rmi://localhost/calificacionesController");
            if (this.puntaje.getText().length() > 0 && this.idAlumno.getText().length() > 0 && this.idExamen.getText().length() > 0) {
                if (this.puntaje.getText().matches("^(([0-9]+)(\\.[0-9]+)?)")) {
                    Examen e = examenController.selectoneExamen(Integer.valueOf(this.idExamen.getText()));
                    Alumno a = alumnoController.selectoneAlumno(Long.valueOf(this.idAlumno.getText()));
                    if (e != null) {
                        if (a != null) {
                            if (banderaCambio == 1) {
                                Calificaciones calificacion = new Calificaciones(idCambio, a, e, Double.valueOf(this.puntaje.getText()));
                                calificacionesController.updateCalificaciones(calificacion);
                                JOptionPane.showMessageDialog(null, "Modificacion exitosa");
                            } else {
                                DefaultListModel dl = calificacionesController.selectallCalificaciones();
                                Calificaciones c = (Calificaciones) dl.get(dl.size() - 1);
                                Calificaciones calificacion = new Calificaciones(c.getIdCalificaciones() + 1, a, e, Double.valueOf(this.puntaje.getText()));
                                calificacionesController.insertCalificaciones(calificacion);
                                JOptionPane.showMessageDialog(null, "Registro exitoso");
                            }
                            this.setVisible(false);
                            this.puntaje.setText("");
                            this.idAlumno.setText("");
                            this.idExamen.setText("");
                            this.jButton4.setText("Aceptar");
                            banderaCambio = 0;
                        } else {
                            JOptionPane.showMessageDialog(null, "id de alumno inv치lido");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "id de examen inv치lido");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "formato de puntaje incorrecto");
                }
            } else {
                JOptionPane.showMessageDialog(null, "completa todos los campos");
            }
        } catch (NotBoundException | MalformedURLException | RemoteException | SecurityException ex) {
            Logger.getLogger(Formulario_Centrodetrabajo_Alta.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HeuristicMixedException ex) {
            Logger.getLogger(Formulario_Calificaciones_Alta.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RollbackException ex) {
            Logger.getLogger(Formulario_Calificaciones_Alta.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HeuristicRollbackException ex) {
            Logger.getLogger(Formulario_Calificaciones_Alta.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SystemException ex) {
            Logger.getLogger(Formulario_Calificaciones_Alta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


