    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (this.jTextField2.getText().length() > 0 && this.jTextField2.getText().length() > 0) {
            String num1 = this.jTextField2.getText(), num2 = this.jTextField3.getText();
            if (num1.matches("^((\\+|-)?([0-9]+)(\\.[0-9]+)?)|((\\+|-)?\\.?[0-9]+)")) {//valida formato de número
                if (num2.matches("^((\\+|-)?([0-9]+)(\\.[0-9]+)?)|((\\+|-)?\\.?[0-9]+)")) {
                    //JOptionPane.showMessageDialog(null,Float.valueOf(num1)+Float.valueOf(num2));
                    abrirSocketOB(num1, num2);
                } else {
                    JOptionPane.showMessageDialog(null, "Segundo numero inválido");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Primer numero inválido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Olvidaste llenar un campo");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

