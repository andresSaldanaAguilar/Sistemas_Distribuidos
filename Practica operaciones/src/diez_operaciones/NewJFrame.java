    private void abrirSocketOB(String num1, String num2) {
        Socket socket = null;
        try {
            socket = new Socket("localhost", 6667);
            PrintStream salida = new PrintStream(socket.getOutputStream());
            String operacion = (String) this.DosNumeros.getItemAt(DosNumeros.getSelectedIndex()); //Nos dice qué operacion es la que hay que hacer
            salida.println(operacion);
            salida.println(num1);
            salida.println(num2);
            //aqui recibo del servidor
            BufferedReader entrada = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            System.out.println("Recibido desde el Servidor:   ");
            String respuesta = entrada.readLine();
            System.out.println("Resultado: " + respuesta);
            JOptionPane.showMessageDialog(null, "Respuesta: "+respuesta);
            entrada.close();
            socket.close();
            //
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                socket.close();
            } catch (IOException ee) {
            }
        }
    }

