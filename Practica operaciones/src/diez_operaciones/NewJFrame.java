    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String numeros = this.jTextField1.getText();
        if (numeros.matches("^(((((\\+|-)?([0-9]+)(\\.[0-9]+)?)|((\\+|-)?\\.?[0-9]+))(\\,))*)|(((\\\\+|-)?([0-9]+)(\\\\.[0-9]+)?)|((\\\\+|-)?\\\\.?[0-9]+))|(((((\\+|-)?([0-9]+)(\\.[0-9]+)?)|((\\+|-)?\\.?[0-9]+))(\\,))*)(((\\+|-)?([0-9]+)(\\.[0-9]+)?)|((\\+|-)?\\.?[0-9]+))")) {
            if (!numeros.endsWith(",")) {
                String[] arreglo_num = numeros.split(",");
                
                XYSeries series = new XYSeries("Grafica de numeros");
                
                for (int i = 0; i < arreglo_num.length; i++) {
                    series.add((double)i,Double.valueOf(arreglo_num[i]));
                }
                
                XYSeriesCollection dataset = new XYSeriesCollection();
                dataset.addSeries(series);
                
                JFreeChart chart = ChartFactory.createXYLineChart("Resultado", "", "cantidad", dataset, PlotOrientation.VERTICAL,true,false,false);
                ChartFrame frame = new ChartFrame("prueba",chart);
                frame.pack();
                frame.setVisible(true);
                
                abrirSocketArreglo(arreglo_num);
            } else {
                JOptionPane.showMessageDialog(null, "Ups... al parecer terminaste con una coma");
            }
        } else {
            JOptionPane.showMessageDialog(null, "formato inválido");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

