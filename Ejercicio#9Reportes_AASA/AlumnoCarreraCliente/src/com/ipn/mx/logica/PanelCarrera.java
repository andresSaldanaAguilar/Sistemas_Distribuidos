    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //agregar
        if(jComboBox1.getSelectedIndex() == 0){
            if(!jTextField1.getText().isEmpty()){
                try {
                    boolean aux = acciones.InsertarCarrera(jTextField1.getText());
                    if(aux){
                        JOptionPane.showMessageDialog(null, "Exito al insertar");
                        refrescar();
                    }else{
                        JOptionPane.showMessageDialog(null, "Error al insertar");
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(PanelCarrera.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        //borrar
        else if (jComboBox1.getSelectedIndex() == 1){
            if(!jTextField2.getText().isEmpty()){
                try {
                    boolean aux = acciones.BorrarCarrera(Integer.parseInt(jTextField2.getText()));
                    if(aux){
                        JOptionPane.showMessageDialog(null, "Exito al borrar");
                        refrescar();
                    }else{
                        JOptionPane.showMessageDialog(null, "Error al borrar");
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(PanelCarrera.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        //actualizar
        else if (jComboBox1.getSelectedIndex() == 2){
            if(!jTextField2.getText().isEmpty() && !jTextField1.getText().isEmpty()){
                try {
                    boolean aux = acciones.ActualizarCarrera(Integer.parseInt(jTextField2.getText()),jTextField1.getText());
                    if(aux){
                        JOptionPane.showMessageDialog(null, "Exito al actualizar");
                        refrescar();
                    }else{
                        JOptionPane.showMessageDialog(null, "Error al actualizar");
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(PanelCarrera.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

