    public void refrescar(){
        try {
            modelo.setRowCount(0);
            List<Carrera> carreras = acciones.Carreras();
            carreras.forEach((carrera) -> {
                modelo.addRow(new Object[]{carrera.getId(),carrera.getNombreCarrera()});
            });     
        } catch (RemoteException ex) {
            Logger.getLogger(PanelCarrera.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }

